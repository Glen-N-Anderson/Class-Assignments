num1 = int(input("Enter your first number: "))#asks user for 1st number
operation = input("Enter your operator(+,-,*,/,%,**): ")#input operator
num2 = int(input("Enter your second number: "))#asks user for 2nd number

if operation=="+":
	print(num1 + num2)
elif operation=="-":
	print(num1 - num2)
elif operation=="*":
	print(num1 * num2)
elif operation=="/":
	print(num1 / num2)
elif operation=="%":
	print(num1 % num2)
elif operation=="**":
	print(num1 ** num2)


	
	
	
	
